# バブルソートで降順に並び替え
score_list = list(scores.items())
n = len(score_list)

for i in range(n):
    for j in range(n - 1 - i):
        total_j = sum(score_list[j][1].values())
        total_next = sum(score_list[j + 1][1].values())
        if total_j < total_next:
            temp = score_list[j]
            score_list[j] = score_list[j + 1]
            score_list[j + 1] = temp

# 結果出力
print("【総合順位】")
for idx, (name, score_dict) in enumerate(score_list, 1):
    total = sum(score_dict.values())
    print(f"{idx}位: {name} 合計: {total:.3f}点")
    for event, score in score_dict.items():
        print(f"    {event}: {score:.3f}点")
    print()
